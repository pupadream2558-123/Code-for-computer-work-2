print("=== ระบบคำนวณราคาประเมินการปูพื้นห้อง ===")
กว้าง = float(input("ความกว้างของห้อง (เมตร): "))
ยาว = float(input("ความยาวของห้อง (เมตร): "))
ราคากระเบื้องต่อตารางเมตร = float(input("ราคากระเบื้อง (บาท ต่อ ตารางเมตร): "))
พื้นที่ห้อง = กว้าง * ยาว
ราคารวมทั้งหมด = พื้นที่ห้อง * ราคากระเบื้องต่อตารางเมตร
print("=== ผลการวิเคราะห์ ===")
print(f"• พื้นที่ห้องของคุณคือ: {พื้นที่ห้อง} ตารางเมตร")
print(f"• งบประมาณค่ากระเบื้องที่ต้องเตรียม: {ราคารวมทั้งหมด:,.2f} บาท")
